
public class Item {
	private int price = 0;
	private int stock = 0;

	// constructor with no parameters or vars.
	public Item() {
		;
	}

	// price setter
	public void setPrice(int mPrice) {
		price = mPrice;
	}

	// price getter
	public int getPrice() {
		return price;
	}

	// stock setter
	public void setStock(int mStock) {
		stock = mStock;
	}

	// stock getter
	public int getStock() {
		return stock;
	}

}
